    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonSalvarActionPerformed
    {//GEN-HEADEREND:event_jButtonSalvarActionPerformed
        if ((jTextFieldCodigo.getText().equals("")) || jTextFieldCodigo.getText().equals(""))
        {
            if ((jComboBoxAno.getSelectedItem().equals("")) || (jComboBoxAssunto.getSelectedItem().equals(""))
                    || (jComboBoxCaixa.getSelectedItem().equals("")) || (jTextFieldInfo.getText().equals("")))
            {
                JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos!");
            } else
            {
                cad.setAno((String) jComboBoxAno.getSelectedItem());
                cad.setCaixa((String) jComboBoxCaixa.getSelectedItem());
                cad.setAssunto((String) jComboBoxAssunto.getSelectedItem());
                cad.setInfo(jTextFieldInfo.getText());

                daoHibernate.salvar(cad);
                JOptionPane.showMessageDialog(rootPane, "Cadastro inserido com sucesso!");
                preencherTabela();

                jTextFieldInfo.setText(null);
                jComboBoxAno.requestFocus();
            }

        } else
        {
            cad.setId(Integer.parseInt(jTextFieldCodigo.getText()));
            cad.setAno((String) jComboBoxAno.getSelectedItem());
            cad.setCaixa((String) jComboBoxCaixa.getSelectedItem());
            cad.setAssunto((String) jComboBoxAssunto.getSelectedItem());
            cad.setInfo(jTextFieldInfo.getText());

            daoHibernate.atualizar(cad);
            JOptionPane.showMessageDialog(rootPane, "Cadastro atualizado com sucesso!");
            preencherTabela();
            limparCampos();

        }

    }//GEN-LAST:event_jButtonSalvarActionPerformed

