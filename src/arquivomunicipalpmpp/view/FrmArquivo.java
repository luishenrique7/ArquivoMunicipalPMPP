    public final void mostraData()
    {
        SimpleDateFormat sdf = new SimpleDateFormat("EEEE, dd 'de' MMMM 'de' yyyy");
        String data = sdf.format(new Date(System.currentTimeMillis()));
        jLabelData.setText(data);
    }

    public void preencherTabela()
    {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]
        {
            "Id", "Ano", "Caixa", "Assunto", "Informações"
        };

        cad.setAno(jTextFieldPesquisar.getText());
        List<Cadastro> cadastro = daoHibernate.listar();

        for (Cadastro c : cadastro)
        {
            dados.add(new Object[]
            {
                c.getId(),
                c.getAno(),
                c.getCaixa(),
                c.getAssunto(),
                c.getInfo()
            });
        }

        ModeloTabela model = new ModeloTabela(dados, Colunas);
        jTableDados.setModel(model);
        jTableDados.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableDados.getColumnModel().getColumn(0).setResizable(false);
        jTableDados.getColumnModel().getColumn(1).setPreferredWidth(50);
        jTableDados.getColumnModel().getColumn(1).setResizable(false);
        jTableDados.getColumnModel().getColumn(2).setPreferredWidth(50);
        jTableDados.getColumnModel().getColumn(2).setResizable(false);
        jTableDados.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTableDados.getColumnModel().getColumn(3).setResizable(false);
        jTableDados.getColumnModel().getColumn(4).setPreferredWidth(300);
        jTableDados.getColumnModel().getColumn(4).setResizable(false);
        jTableDados.getTableHeader().setReorderingAllowed(false);
        jTableDados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        jTableDados.isCellEditable(WIDTH, WIDTH);
    }

